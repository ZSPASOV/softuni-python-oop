# What is Encapsulation?
# Encapsulation is an essential aspect of object-oriented programming
# Encapsulation is the packing of data and functions into a single component
# Allows us to put restrictions and can prevent the accidental modification of data
# With encapsulation we can hide information
# This means delimiting the internal interface and attributing from the external world
# The benefits of information hiding are:
# reducing system complexity
# increasing robustness
# Python implements weak encapsulation. This means it is performed by convention
# rather than being enforced by the language
# In Python there are no access modifiers and everything written
# with in the class (methods and variables) are public by default
# How to control access?
# Using single underscore
# Using double underscore (making it private)
# Using getter and setter methods to access private variables

